                <p>A CALVÍCIE, OU ALOPECIA ANDROGENÉTICA, LIGADA À HERANÇA GENÉTICA DOS PAIS É A PRINCIPAL CAUSA DA
                    PERDA PERMANENTE DE CABELO DO COURO CABELUDO EM HOMENS A PARTIR DOS 30 ANOS DE IDADE E EM MULHERES A
                    PARTIR DOS 40 ANOS, SENDO MAIS PREVALENTE NO PÚBLICO MASCULINO. AS ÁREAS MAIS AFETADAS SÃO: REGIÃO
