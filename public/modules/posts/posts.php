            <p class="marginTop60px">O transplante capilar FUE (follicular unit extraction ou extração de unidade
                folicular uma a uma) é o
                tratamento definitivo da calvície, para homens e mulheres. É uma técnica moderna e minuciosa, sem cortes
                e com cicatrizes milimétricas imperceptíveis ao olho nu. A técnica é realizada na própria Clínica
                Corcini, com todo o conforto. A anestesia é local e o paciente poderá interagir com a equipe durante
                todo o procedimento, ou, se preferir, poderá ser medicado para dormir e relaxar, conforme prescrição da
                Dra. Ana Corcini. A sensibilidade no couro cabeludo é baixa na maioria dos pacientes, sendo um
                procedimento muito confortável em relação a dor.</p>

            <p class="marginTop60px">O transplante apresenta 2 períodos de cirurgia, o primeiro no qual que se extraem
                os folículos, e o
                segundo no qual é realizada a implantação dos pelos para a região da calvície. Todo esse processo dura
                em média 7h. </p>

