            <h1 class="marginTop60px">Transplante capilar FUE</h1>
            <h2 class="marginTop60px">TRANSPLANTE CAPILAR FUE: EQUIPE TREINADA COM TÉCNICA DA TURQUIA PARA UMA PRÁTICA
                DE EXCELÊNCIA, COM
                RESULTADOS NATURAIS E MÁXIMA IMPLANTAÇÃO DE FIOS. </h2>
            <p class="marginTop60px">A Tricologia é a ciência médica que estuda os cabelos. Os fios possuem ciclos de
                vida, com fase de
                crescimento, repouso e queda. O processo natural é uma queda de 100 a 150 fios ao dia, ultrapassando
                esse número nos deparamos com um quadro de alopecia, perda excessiva do cabelo. Existem alguns fatores
                que contribuem para essa queda, como a genética, estresse, falta de vitaminas, dano térmico e químico,
                agrotóxicos, entre outros. </p>
